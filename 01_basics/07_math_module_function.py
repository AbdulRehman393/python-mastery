import math

print(math.pi)    # pi
print(math.e)     # exponential constant

x = 9
y = 9.6

# square root function
result = math.sqrt(x)
print(result)

# hypotenuse function
result = math.hypot(x,y)
print(result)

# ceiling will always round a floating point number up
result = math.ceil(y)
print(result)

# floor will always a number down
result = math.floor(y)
print(result)
