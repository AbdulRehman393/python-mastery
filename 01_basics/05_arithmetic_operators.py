# ---------------------------- Arithmetic Operators -------------------------------------------------------
friends = 10

# Addition
# friends = friends + 1
# friends += 1           # +=  is augmented assignment operator  friends += 1  is same as friends = friends + 1

# Subtraction
# friends = friends - 2
# friends -= 2

# Multiplication
# friends = friends * 3
# friends *= 3

# Division / , it will give us float
# friends = friends / 2
# friends /= 2

# Division // , it will give us integer
#friends = friends // 2
# friends //= 2

# Exponents
# friends = friends ** 2
# friends **= 2


# Modulus , it gives you remainder of any division
remainder = friends % 3

print(remainder)


